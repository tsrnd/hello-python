a = 5
b = 10

# Addition
def addition():
    return a + b

# Subtraction
def subtraction():
    return a - b

# Multiple
def multiple():
    return a * b

# Division 
def division():
    return a / b

# Modulus
def modulus():
    return a % b

# Exponentiation
def exponentiation():
    return a ** b

# Floor division	
def floorDivision():
    return a // b



